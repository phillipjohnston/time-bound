class TestMain:
    def _run(self, argv, **patches):
        """Run main() with all subprocess calls patched."""
        defaults = {
            "find_claude": "/usr/bin/claude",
            "stage_all": (0, "", ""),
            "get_staged_diff": (0, "diff content", ""),
            "call_claude": (0, "Fix the bug", ""),
            "do_commit": (0, "[main abc]", ""),
        }
        defaults.update(patches)

        with patch("bin.smart_commit.find_claude", return_value=defaults["find_claude"]), \
             patch("bin.smart_commit.stage_all", return_value=defaults["stage_all"]), \
             patch("bin.smart_commit.get_staged_diff", return_value=defaults["get_staged_diff"]), \
             patch("bin.smart_commit.call_claude", return_value=defaults["call_claude"]), \
             patch("bin.smart_commit.do_commit", return_value=defaults["do_commit"]):
            return main(argv)

    def test_no_staged_changes_exits_0(self):
        rc = self._run([], get_staged_diff=(0, "", ""))
        assert rc == 0

    def test_claude_not_found_exits_1(self):
        rc = self._run([], find_claude=None)
        assert rc == 1

    def test_stage_all_failure_exits_1(self):
        rc = self._run(["--stage-all"], stage_all=(1, "", "permission denied"))
        assert rc == 1

    def test_git_diff_failure_exits_1(self):
        rc = self._run([], get_staged_diff=(128, "", "not a repo"))
        assert rc == 1

    def test_claude_failure_exits_1(self):
        rc = self._run([], call_claude=(1, "", "timed out"))
        assert rc == 1

    def test_claude_empty_output_exits_1(self):
        rc = self._run([], call_claude=(0, "   ", ""))
        assert rc == 1

    def test_dry_run_does_not_commit(self):
        with patch("bin.smart_commit.find_claude", return_value="/usr/bin/claude"), \
             patch("bin.smart_commit.get_staged_diff", return_value=(0, "diff", "")), \
             patch("bin.smart_commit.call_claude", return_value=(0, "Fix bug", "")), \
             patch("bin.smart_commit.do_commit") as mock_commit:
            rc = main(["--dry-run"])
            assert rc == 0
            mock_commit.assert_not_called()

    def test_successful_commit_returns_0(self):
        rc = self._run([])
        assert rc == 0

    def test_commit_failure_exits_1(self):
        rc = self._run([], do_commit=(1, "", "pre-commit hook failed"))
        assert rc == 1

    def test_stage_all_then_empty_diff_exits_0(self):
        rc = self._run(["--stage-all"], stage_all=(0, "", ""), get_staged_diff=(0, "", ""))
        assert rc == 0

    def test_verbose_prints_diff(self, capsys):
        self._run(["-v"])
        captured = capsys.readouterr()
        assert "diff content" in captured.out

    def test_stage_all_calls_stage(self):
        with patch("bin.smart_commit.find_claude", return_value="/usr/bin/claude"), \
             patch("bin.smart_commit.stage_all", return_value=(0, "", "")) as mock_stage, \
             patch("bin.smart_commit.get_staged_diff", return_value=(0, "diff", "")), \
             patch("bin.smart_commit.call_claude", return_value=(0, "Fix bug", "")), \
             patch("bin.smart_commit.do_commit", return_value=(0, "", "")):
            main(["--stage-all"])
            mock_stage.assert_called_once()

    def test_uses_cwd_by_default(self):
        with patch("bin.smart_commit.find_claude", return_value="/usr/bin/claude"), \
             patch("bin.smart_commit.get_staged_diff", return_value=(0, "diff", "")) as mock_diff, \
             patch("bin.smart_commit.call_claude", return_value=(0, "Fix bug", "")), \
             patch("bin.smart_commit.do_commit", return_value=(0, "", "")):
            main([])
            assert mock_diff.call_args[0][0] == os.getcwd()
