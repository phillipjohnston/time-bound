class TestFindClaude:
    def test_uses_cli_arg_first(self):
        with patch.dict(os.environ, {"CLAUDE_PATH": "/env/claude"}):
            with patch("bin.smart_commit.shutil.which", return_value="/which/claude"):
                assert find_claude("/arg/claude") == "/arg/claude"

    def test_uses_env_var_when_no_arg(self):
        with patch.dict(os.environ, {"CLAUDE_PATH": "/env/claude"}):
            with patch("bin.smart_commit.shutil.which", return_value="/which/claude"):
                assert find_claude(None) == "/env/claude"

    def test_falls_back_to_which(self):
        env = {k: v for k, v in os.environ.items() if k != "CLAUDE_PATH"}
        with patch.dict(os.environ, env, clear=True):
            with patch("bin.smart_commit.shutil.which", return_value="/which/claude"):
                assert find_claude(None) == "/which/claude"

    def test_returns_none_when_not_found(self):
        env = {k: v for k, v in os.environ.items() if k != "CLAUDE_PATH"}
        with patch.dict(os.environ, env, clear=True):
            with patch("bin.smart_commit.shutil.which", return_value=None):
                assert find_claude(None) is None


class TestGetStagedDiff:
    def test_returns_diff_on_success(self):
        with patch("bin.smart_commit.run_command", return_value=(0, "diff text", "")) as mock_rc:
            rc, diff, err = get_staged_diff("/repo")
            assert rc == 0
            assert diff == "diff text"
            mock_rc.assert_called_once_with(["git", "diff", "--staged"], cwd="/repo")

    def test_returns_error_on_failure(self):
        with patch("bin.smart_commit.run_command", return_value=(128, "", "not a git repo")):
            rc, _, stderr = get_staged_diff("/repo")
            assert rc == 128
            assert "not a git repo" in stderr


