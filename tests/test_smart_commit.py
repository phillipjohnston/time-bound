class TestBuildPrompt:
    def test_contains_diff(self):
        diff = "--- a/foo.py\n+++ b/foo.py\n@@ -1 +1 @@ some change"
        prompt = build_prompt(diff)
        assert diff in prompt

    def test_no_attribution_keywords(self):
        prompt = build_prompt("some diff")
        lower = prompt.lower()
        assert "generated by" not in lower
        assert "anthropic" not in lower

    def test_imperative_rule_present(self):
        prompt = build_prompt("diff")
        assert "imperative" in prompt.lower()

    def test_no_fence_rule_present(self):
        prompt = build_prompt("diff")
        assert "fences" in prompt.lower() or "markdown" in prompt.lower()


class TestCallClaude:
    def test_passes_prompt_as_p_flag(self):
        with patch("bin.smart_commit.run_command", return_value=(0, "Add feature", "")) as mock_rc:
            call_claude("/usr/bin/claude", "my prompt", "/repo")
            args = mock_rc.call_args[0][0]
            assert args[0] == "/usr/bin/claude"
            assert "-p" in args
            assert "my prompt" in args

    def test_passes_output_format_text(self):
        with patch("bin.smart_commit.run_command", return_value=(0, "msg", "")) as mock_rc:
            call_claude("/usr/bin/claude", "prompt", "/repo")
            args = mock_rc.call_args[0][0]
            assert "--output-format" in args
            assert "text" in args

    def test_uses_120s_timeout(self):
        with patch("bin.smart_commit.run_command", return_value=(0, "msg", "")) as mock_rc:
            call_claude("/usr/bin/claude", "prompt", "/repo")
            assert mock_rc.call_args[1]["timeout"] == 120

    def test_failure_propagates(self):
        with patch("bin.smart_commit.run_command", return_value=(1, "", "API error")):
            rc, _, stderr = call_claude("/usr/bin/claude", "prompt", "/repo")
            assert rc == 1
            assert "API error" in stderr


class TestStripFences:
    def test_strips_fenced_code_block(self):
        text = "```\nFix the bug\n```"
        assert _strip_fences(text) == "Fix the bug"

    def test_strips_fenced_with_language(self):
        text = "```text\nAdd feature\n\nBody here\n```"
        assert _strip_fences(text) == "Add feature\n\nBody here"

    def test_leaves_plain_text_alone(self):
        text = "Fix the login flow"
        assert _strip_fences(text) == text

    def test_leaves_single_line_alone(self):
        text = "```only one fence"
        assert _strip_fences(text) == text


